#include	"libft.h" /*LIBRERIA CUSTOM*/

/*
AÃ±ade el nodo "new" al final de la lista "lst".
*/
void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;

	if (lst)
	{
		if (*lst)
		{
			last = ft_lstlast(*lst);
			last->next = new;
		}
		else
			*lst = new;
	}
}
