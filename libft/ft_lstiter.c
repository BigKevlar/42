#include	"libft.h" /*LIBRERIA CUSTOM "jmartos-"*/

/*#Itera (avanza) en la lista "lst" y aplica la funciÃ³n "f" en el
contenido de cada nodo.#*/
void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	while (lst)
	{
		(*f)(lst->content);
		lst = lst->next;
	}
}

