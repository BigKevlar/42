#include	"libft.h" /*LIBRERIA CUSTOM "jmartos-"*/

/*#Devuelve el Ãºltimo nodo de la lista. La parte de "lst = lst->next" va
avanzando hasta toparse con el ultimo nodo.#*/
t_list	*ft_lstlast(t_list *lst)
{
	while (lst)
	{
		if (!lst->next)
			return (lst);
		lst = lst->next;
	}
	return (lst);
}
